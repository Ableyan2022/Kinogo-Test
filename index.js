function closeBottomFilterMenu() {
  bottomFilterMenu.style.display = `none`
  let activeBottomFilter = document.querySelector(".activeBottomFilter")
  activeBottomFilter.classList.remove("activeBottomFilter")
}

function clearButton() {
  let activeButtons = document.querySelectorAll(".bottomFilterMenuButtons .is_active")
  activeButtons.forEach((el) => el.classList.remove("is_active"))
  renderClearButton()
}

function renderClearButton() {
  let count = 0
  let activeButtons = document.querySelectorAll(".bottomFilterMenuButtons .is_active")
  activeButtons.forEach(() => count++)

  if(count > 0) {
    bottomMenuForm.innerHTML = `
    <div>
      <button id="ApplyButton">Apply</button>
      <button onclick="closeBottomFilterMenu()">Cancel</button>
    </div>
    <div>  
      <button id="clearButton" onclick="clearButton()">Clear(<span>${count}</span>)</button>
    </div>
    `
  } else {
    bottomMenuForm.innerHTML = `
    <div>
      <button id="ApplyButton">Apply</button>
      <button onclick="closeBottomFilterMenu()">Cancel</button>
    </div>
    `
  }
}